package com.danosoftware.galaxyforce.sprites.properties;

public class SpriteDimensions {

  private final int width;
  private final int height;

  public SpriteDimensions(int width, int height) {
    this.width = width;
    this.height = height;
  }

  public int getWidth() {
    return width;
  }

  public int getHeight() {
    return height;
  }
}
